from openerp.osv import osv, fields
import time
from openerp.tools.translate import _


class PurchaseOrderLine(osv.osv):
    
    _inherit = 'purchase.order.line'
    
    _columns = {
                'prod_type':fields.selection([('inventory','Inventory')
                                                 ,('non-inventory','Non Inventory')
                                                 ,('asset','Asset')
                                                 ,('service','service')], string="Product Type"),
                }
    
    def onchange_product_id(self, cr, uid, ids, pricelist_id, product_id, qty, uom_id,
            partner_id, date_order=False, fiscal_position_id=False, date_planned=False,
            name=False, price_unit=False, state='draft', context=None):
        
        res = super(PurchaseOrderLine, self).onchange_product_id(cr, uid, ids, pricelist_id, product_id, qty, uom_id, partner_id, date_order=date_order, fiscal_position_id=fiscal_position_id, date_planned=date_planned, name=name, price_unit=price_unit, state=state, context=context)
        if product_id:
            product_type = self.pool.get('product.product').read(cr, uid, product_id, ['product_type'], context=context)['product_type']
            if not product_type:
                raise osv.except_osv(_('ERROR !!'),_('Please select the product type in product form, before proceeding to purchase.'))
            res['value'].update({'prod_type':product_type})
        return res

